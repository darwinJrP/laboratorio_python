""" Tipos de datos trascendentales """
""" Tipos de datos: Lista []"""
var1 = [20, "Luis", "Calle x", 1993]
print(f"El valor de mi variable var1 es: {var1}")

""" Tipos de datos: Tupla () """
var2 = (40, 80.9, 170, 2024, "Hi otp graves")
print(f"El valor de mi tupla es: {var2}")

""" Tipos de datos: Set() """
var3 = set(["Renan", 50, 150, 3.14159265358979, False, 50, 50, 150])
print(f"El valor de mi variable var3 es como conjunto {var3}")

""" Tipos de datos: diccionario {} """
var4 = {"nombre": "Mario", "edad": 28, "dni": 111111}
print(f"El valor de mi variable var4 es como diccionario {var4}")
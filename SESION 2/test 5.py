""" Listas en Python """

""" Listas pop(): Quita un elemento en una posición dada """

lista = ["Perú", "España", "Canadá", "Argentina"]
print(f"Mi lista inicial es: {lista}")

lista.pop()

print(f"Mi lista actualizada es:{lista} ")

lista.pop(1)
print(f"Mi lista actualizada es la siguiente: {lista}")
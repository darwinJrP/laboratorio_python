""" Listas en Python """
""" Listas len(): Obtener el tamaño de la lista en Python """
paises = []

print(f"El tamaño de mi lista es: {len(paises)}")

paises.append("Perú")
paises.append("Italia")
paises.append("España")

print(f"Los valores de mi lista de paises actual es:{paises}")
print(f"El tamaño de mi lista actual es: {len(paises)}")

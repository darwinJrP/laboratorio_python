''' Operadores aritméticos con Python '''

""" Operador potencia: ** """

potencia_1 = 10**3
print(f"El resultado de mi potencia_1 es: {potencia_1}")

#segunda forma
""" Usando la palabra reservada 'pow' podemos también elevar a un número a cierta potencia """
""" Nos va a pedir dos valores, el primero es la base y el segundo valor es la potencia a la que se elevará """
potencia_2 = pow(20,4)
potencia_3 = pow(4,5)
print(f"El valor de mi potencia_2 es: {potencia_2}")
print(f"El valor de mi potencia_3 es: {potencia_3}")
